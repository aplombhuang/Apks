package team4.drugapp;



public class UserInformation {

    public String name;
    public String address;
    public String gender;
    public String weight;
    public String age;

    public UserInformation(){

    }

    public UserInformation(String name, String address, String gender, String weight, String age) {
        this.name = name;
        this.address = address;
        this.gender = gender;
        this.weight = weight;
        this.age = age;
    }
}
